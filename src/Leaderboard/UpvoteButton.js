import React from 'react'

class UpvoteButtons extends React.Component {
    constructor(props) {
        super(props)
        this.state = { i: 0 }
    }

    updateCount = () => {
        this.setState(prvState => ({
            i: prvState.i + 1
        }))
    }

    render() {
        return (
            <>
                <button onClick={e => this.updateCount()}>Upvote {this.state.i}</button>
            </>
        )
    }
}

export default UpvoteButtons